struct PointLights {
	std::vector<glm::vec3> positions;
	std::vector<glm::vec3> colors;
};

// create a set of random light sources
PointLights CreateRandomPointLights(const unsigned int NUM_LIGHTS) {
	std::vector<glm::vec3> lightPositions;
	std::vector<glm::vec3> lightColors;
	srand(13);
	for (int i = 0; i < NUM_LIGHTS; i++) {
		// calculate slightly random offsets
		float xPos = ((rand() % 100) / 100.0) * 6.0 - 3.0;
		float yPos = ((rand() % 100) / 100.0) * 6.0 - 4.0;
		float zPos = ((rand() % 100) / 100.0) * 6.0 - 3.0;
		lightPositions.push_back(glm::vec3(xPos, yPos, zPos));
		// also calculate random color
		float rColor = ((rand() % 100) / 200.0f) + 0.5; // between 0.5 and 1.0
		float gColor = ((rand() % 100) / 200.0f) + 0.5; // between 0.5 and 1.0
		float bColor = ((rand() % 100) / 200.0f) + 0.5; // between 0.5 and 1.0
		lightColors.push_back(glm::vec3(rColor, gColor, bColor));
	}

	PointLights result;
	result.positions = lightPositions;
	result.colors = lightColors;
	return result;
}

struct DirectionalLights {
	std::vector<glm::vec3> directions;
	std::vector<glm::vec3> colors;
};

DirectionalLights CreateSingleDirectionalLight(const glm::vec3 direction, const glm::vec3 color) {
	std::vector<glm::vec3> directions;
	std::vector<glm::vec3> colors;

	directions.push_back(direction);
	colors.push_back(color);

	DirectionalLights result;
	result.directions = directions;
	result.colors = colors;
	return result;
}